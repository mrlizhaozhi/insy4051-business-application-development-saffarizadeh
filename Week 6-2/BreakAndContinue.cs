using System;

namespace ConsoleApp
{
    class Program
    {
        static void Main(string[] args)
        {
            // Using break
            for (int i = 0; i < 10; i++)
            {
                if (i == 4)
                {
                    break;
                }
                Console.WriteLine(i);
            }

            // Using continue
            for (int i = 0; i < 10; i++)
            {
                if (i == 4)
                {
                    continue;
                }
                Console.WriteLine(i);
            }
        }

    }
}
